public class Assignment {
    private double grade;
    private int weight;

    public Assignment(double grade, int weight) {
        this.grade = grade;
        this.weight = weight;
    }

    public double getGrade() {
        return grade;
    }

    public int getWeight() {
        return weight;
    }
}
